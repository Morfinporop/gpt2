// Обратный словарь: число -> символ
export const REVERSE_SYMBOLS: { [key: number]: string } = {};
for (const [char, num] of Object.entries(SYMBOLS)) {
  REVERSE_SYMBOLS[num] = char;
}

// Текст в числа
export function textToNumbers(text: string): number[] {
  const result: number[] = [];
  for (const char of text) {
    if (SYMBOLS[char] !== undefined) {
      result.push(SYMBOLS[char]);
    } else {
      result.push(0); // Неизвестный символ
    }
  }
  return result;
}

// Числа в текст
export function numbersToText(numbers: number[]): string {
  return numbers.map(n => REVERSE_SYMBOLS[n] || '').join('');
}

// Слово в вектор (среднее значение символов)
export function wordToVector(word: string): number {
  const nums = textToNumbers(word.toLowerCase());
  if (nums.length === 0) return 0;
  return nums.reduce((a, b) => a + b, 0) / nums.length;
}
